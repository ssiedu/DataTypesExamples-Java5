public class ControlFlowDemoOne {
    public static void main(String args[]){
        
        int marks=45;
        
        if(marks>=60){
            System.out.println("DIV-I");
        }else if(marks>=45){
            System.out.println("DIV-II");
        }else if(marks>=33){
            System.out.println("DIV-III");
        }else{
            System.out.println("FAIL");
        }
        
    }
}
